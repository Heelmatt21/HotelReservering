package DesignPatterns.StructuralPattern.Airco;

public class Cooler {
    public void on(){
        System.out.println("Cooler on.");
    }

    public void low(){
        System.out.println("Cooler is on low speed.");
    }

    public void mediuw(){
        System.out.println("Cooler is on medium speed.");
    }

    public void high(){
        System.out.println("Cooler is on high speed.");
    }

    public void off(){
        System.out.println("Cooler is off.");
    }
}
